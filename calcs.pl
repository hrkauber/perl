#!/usr/bin/perl

# Variable declaration 
$foo = 5;
$bar = 8; 
$result; 

#Doing math!
#Addition
$result = $foo + $bar; 
print "$foo + $bar = $result\n"; 

#Subtraction
$result = $foo - $bar;
print "$foo - $bar = $result\n";

#Multipication
$result = $foo * $bar; 
print "$foo * $bar = $result\n";

#Division
$result = $foo / $bar; 
print "$foo / $bar = $result\n";

#Modulus
$result = $foo % $bar; 
print "$foo % $bar = $result\n"; 

